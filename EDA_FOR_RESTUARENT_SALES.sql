-- Revenue Contribution By Category --
SELECT 
m.category,
SUM(m.price) AS revenue,
ROUND(
SUM(m.price) * 100 / SUM(SUM(m.price)) OVER(),
2
) AS revenue_percentage
FROM menu_items m
JOIN order_details o
ON m.menu_item_id = o.item_id
GROUP BY m.category
ORDER BY revenue DESC;
-- Insight: Majority of the revenue contribution is generated by italian food items with a 31% of the revenue and asian food items with a 29.3% of the revenue  --
